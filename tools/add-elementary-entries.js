function promptFor(unit, kind, page) {
  if (kind === 'cover') {
    return `溫暖繪本風，${commonCharacters}一起進行「${unit.title}」自然科學觀察，畫面呈現${unit.subtopic}的核心元素，明亮柔和色彩，16:9。`;
  }
  if (kind === 'diagram') {
    return `清楚的科學示意圖，主題為「${unit.title}」，整理${unit.subtopic}，繁體中文標籤，手繪繪本風，背景乾淨。`;
  }
  if (kind === 'activity') {
    return `${unit.activityName}活動插圖，學生使用材料進行觀察與紀錄，${commonCharacters}在自然教室或校園中合作學習，溫暖繪本風。`;
  }
  return `繪本內頁風格，${page[0]}，${page[1]}，${commonCharacters}表情生動，溫暖自然科學繪本風，16:9。`;
}

